#tipo de datos en python

#Strings
print("Hello Word")
print("""Hello Word""")
print('Hello Word')
print('''Hello Word''')

#unir dos palabra - concaternar
print("Bye" + "World")

#Numeros 
a=30
print(a)

#list
print([10, 10, 30, 30])
print(type([10, 10, 30, 30]))

#tupla - no puede cambiar sus valores
print((10, 10, 30, 30))
print(type((10, 10, 30, 30)))

#dictorionies
print({
    "name": "Jeymar",
    "lastname":"Herrera",
    "nickname":"Jey"
})
print(type({
    "name": "Jeymar",
    "lastname":"Herrera",
    "nickname":"Jey"
}))

